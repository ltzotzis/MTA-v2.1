  template<int dim>
  void HeatEquation<dim>::assemble_nonlinear_predictor()
  {
    TimerOutput::Scope t(computing_timer,"Assemble nonlinear predictor system/rhs");
    predictor_system_matrix=0.0;
    mass_matrix=0.0;
    laplace_matrix=0.0;

    const QGauss<dim> quadrature_formula(degree+2);
    const QGauss<dim-1> face_quadrature_formula(degree+2);

    FEValues<dim> fe_values (fe, quadrature_formula, update_values | update_gradients | update_quadrature_points | update_JxW_values);
    FEFaceValues<dim> fe_face_values (fe,face_quadrature_formula, update_values | update_quadrature_points | update_normal_vectors |
      update_JxW_values);

    const unsigned int dofs_per_cell = fe.dofs_per_cell;
    const unsigned int n_q_points = quadrature_formula.size();
    const unsigned int n_face_q_points = face_quadrature_formula.size();

    FullMatrix<double> cell_mass_matrix (dofs_per_cell, dofs_per_cell);
    FullMatrix<double> cell_laplace_matrix(dofs_per_cell,dofs_per_cell);
    std::vector<double> old_solution_values(n_q_points);
    std::vector<types::global_dof_index> local_dof_indices(dofs_per_cell);

    typename DoFHandler<dim>::active_cell_iterator
    cell = dof_handler.begin_active(),
    endc = dof_handler.end();
    for (; cell!=endc; ++cell)
    {
      if (cell->is_locally_owned())
      {
        unsigned int cell_id = cell->material_id();
        ComponentInfo* material_properties = readXMLHierarchy[cell_id];

        double k0 = material_properties->thermalconductivity*1e-9;
        double k1 = material_properties->thermalconductivityalpha*1e-9;
        double c0= material_properties->volumetricheatcapacity*1e-27;
        double c1 = material_properties->volumetricheatcapacitybeta*1e-27;

        cell_mass_matrix = 0.;
        cell_laplace_matrix =0.;

        fe_values.reinit (cell);
        fe_values.get_function_values(old_solution,old_solution_values);

        for(unsigned int q_point=0; q_point < n_q_points; ++q_point)
        {
          const double cv = c0 + c1*old_solution_values[q_point];
          const double kappa = k0 + k1*old_solution_values[q_point];

          for(unsigned int i=0; i<dofs_per_cell; ++i)
          {
            for(unsigned int j=0; j<dofs_per_cell; ++j)
            {
              cell_mass_matrix(i,j) += (cv * fe_values.shape_value(i,q_point) * fe_values.shape_value(j,q_point) * fe_values.JxW(q_point));

              cell_laplace_matrix(i,j) += (kappa * fe_values.shape_grad(i,q_point) * fe_values.shape_grad(j,q_point) *
                fe_values.JxW(q_point));
            }
          }
        }
        for (unsigned int face_number=0; face_number<GeometryInfo<dim>::faces_per_cell; ++face_number)
          if(cell->face(face_number)->at_boundary() && (cell->face(face_number)->boundary_id() == TopId) )
          {
            double eta_top = rootComponentInput->heattransfercoefficientTop*1e-18;
            fe_face_values.reinit(cell,face_number);

            for (unsigned int q_point=0; q_point<n_face_q_points; ++q_point)
              for (unsigned int i=0; i<dofs_per_cell; ++i)
              {
                for (unsigned int j=0; j<dofs_per_cell; ++j)
                  cell_laplace_matrix(i,j) += (eta_top * fe_face_values.shape_value(i,q_point) *  fe_face_values.shape_value(j,q_point) *
                    fe_face_values.JxW(q_point));
              }
          }
          else if(cell->face(face_number)->at_boundary() && (cell->face(face_number)->boundary_id() == BottomId))
          {
            double eta_bottom = rootComponentInput->heattransfercoefficientBottom*1e-18;
            fe_face_values.reinit(cell,face_number);

            for (unsigned int q_point=0; q_point<n_face_q_points; ++q_point)
              for (unsigned int i=0; i<dofs_per_cell; ++i)
              {
                for (unsigned int j=0; j<dofs_per_cell; ++j)
                  cell_laplace_matrix(i,j) += (eta_bottom * fe_face_values.shape_value(i,q_point) *  fe_face_values.shape_value(j,q_point) *
                   fe_face_values.JxW(q_point));
              }
          }
          else if(cell->face(face_number)->at_boundary() && (cell->face(face_number)->boundary_id() == SideId))
          {
            double eta_side = rootComponentInput->heattransfercoefficientSide*1e-18;
            fe_face_values.reinit(cell,face_number);

            for (unsigned int q_point=0; q_point<n_face_q_points; ++q_point)
              for (unsigned int i=0; i<dofs_per_cell; ++i)
              {
                for (unsigned int j=0; j<dofs_per_cell; ++j)
                  cell_laplace_matrix(i,j) += (eta_side * fe_face_values.shape_value(i,q_point) *  fe_face_values.shape_value(j,q_point) * fe_face_values.JxW(q_point));
              }
          }
      }
      cell->get_dof_indices(local_dof_indices);
      constraints.distribute_local_to_global(cell_mass_matrix,local_dof_indices,mass_matrix);
      constraints.distribute_local_to_global(cell_laplace_matrix,local_dof_indices,laplace_matrix);
    }
    laplace_matrix.compress (VectorOperation::add);
    mass_matrix.compress (VectorOperation::add);

    PETScWrappers::MPI::Vector tmp(locally_owned_dofs,MPI_COMM_WORLD);

    if (timestep_method== "be")
    {
      predictor_system_matrix.copy_from(mass_matrix);
      predictor_system_matrix.compress(VectorOperation::add);

      mass_matrix.vmult(pred_system_rhs,old_solution);
      tmp=0.0;
      laplace_matrix.vmult(tmp,old_solution);
      pred_system_rhs.add(-time_step,tmp);
      tmp=0.0;
      create_rhs(tmp,I2,I1);
      pred_system_rhs.add(time_step,tmp);
      pred_system_rhs.compress(VectorOperation::add);
    }
    else if (timestep_method== "bdf2")
    {
      predictor_system_matrix.copy_from(mass_matrix);
      predictor_system_matrix.compress(VectorOperation::add);
      double alpha = time_step/prev_time_step;

      mass_matrix.vmult(pred_system_rhs,old_solution); // compute b=Mu(n)
      pred_system_rhs*=(1-alpha*alpha); // b= Mu(n)-alpha^2Mu(n)
      tmp=0.0;
      create_rhs(tmp,I2,I1); // compute fn=f(t(n))
      pred_system_rhs.add((1+alpha)*time_step,tmp); // compute b= Mu(n)-alpha^2Mu(n)+dt(1+alpha)f(t(n))

      tmp = 0.0;
      laplace_matrix.vmult(tmp,old_solution); // compute tmp=Ku(n)
      pred_system_rhs.add(-(1+alpha)*time_step,tmp); // compute b=Mu(n)-alpha^2Mu(n)+dt(1+alpha)f(t(n))-dt(1+alpha)Ku(n)

      tmp = 0.0;
      mass_matrix.vmult(tmp,old_old_solution); // compute tmp=Mu(n-1)
      pred_system_rhs.add(alpha*alpha,tmp); // b= Mu(n)-alpha^2Mu(n)+dt(1+alpha)f(t(n))-dt(1+alpha)Ku(n)+alpha^2Mu(n-1)
      pred_system_rhs.compress(VectorOperation::add); //SL
    }
    else if (timestep_method == "tr" || timestep_method == "stabtr")
    {
      predictor_system_matrix.copy_from(mass_matrix);
      predictor_system_matrix.compress(VectorOperation::add);
      double alpha = time_step/prev_time_step;

      tmp = 0.0;
      mass_matrix.vmult(pred_system_rhs,old_solution); // b=Mu(n)
      laplace_matrix.vmult(tmp,old_solution); // compute tmp=Ku(n)
      pred_system_rhs.add(-time_step*(2+ alpha)/2,tmp); // compute  b=Mu(n)-dt/2(2+alpha)Ku(n)

      tmp = 0.0;
      laplace_matrix.vmult(tmp,old_old_solution); // compute tmp=Ku(n-1)
      pred_system_rhs.add(time_step*alpha/2,tmp); // compute b=Mu(n)-dt/2(2+alpha)Ku(n)+(dt/2)alpha*Ku(n-1)

      tmp=0.0;
      create_rhs(tmp,I2,I1); // compute fn=f(t(n)
      pred_system_rhs.add(time_step*(2+alpha)/2,tmp); // compute b=Mu(n)+dt/2(2+alpha)[f(t(n)-(Ku(n)]+(dt/2)alpha*Ku(n-1)

      tmp = 0.0;
      create_rhs(tmp,I3,I2); // compute tmp=f(t(n-1))
      pred_system_rhs.add(-time_step*alpha/2,tmp); // compute b=Mu(n)+dt/2(2+alpha)[f(t(n)-(Ku(n)]-(dt/2)alpha[f(t(n-1)-Ku(n-1)]
      pred_system_rhs.compress(VectorOperation::add); //SL
    }
    else if (timestep_method == "imr") // AB3 PREDICTOR
    {
      predictor_system_matrix.copy_from(mass_matrix);
      predictor_system_matrix.compress(VectorOperation::add);

      tmp = 0.0;
      mass_matrix.vmult(pred_system_rhs,old_solution);
      laplace_matrix.vmult(tmp,old_solution);
      pred_system_rhs.add(-23*time_step/12,tmp);

      tmp = 0.0;
      laplace_matrix.vmult(tmp,old_old_old_solution);
      pred_system_rhs.add(4*time_step/3,tmp);

      tmp = 0.0;
      laplace_matrix.vmult(tmp,old_old_solution);
      pred_system_rhs.add(-5*time_step/12,tmp);

      tmp=0.0;
      create_rhs(tmp,I2,I1);
      pred_system_rhs.add(23*time_step/12,tmp);

      tmp = 0.0;
      create_rhs(tmp,I3,I2);
      pred_system_rhs.add(-4*time_step/3,tmp);

      tmp = 0.0;
      create_rhs(tmp,I4,I3);
      pred_system_rhs.add(5*time_step/12,tmp);
      pred_system_rhs.compress(VectorOperation::add); //SL
    }
  }

  template<int dim>
  void HeatEquation<dim>::solve_nonlinear_predictor()
  {
    TimerOutput::Scope t(computing_timer,"Solve nonlinear predictor");
    PETScWrappers::PreconditionJacobi pre_precond;
    
    if (gpu) {
      MatSetType(predictor_system_matrix.petsc_matrix(), MATMPIAIJCUSPARSE);
      MatAssemblyBegin(predictor_system_matrix.petsc_matrix(), MAT_FINAL_ASSEMBLY);
      MatAssemblyEnd(predictor_system_matrix.petsc_matrix(), MAT_FINAL_ASSEMBLY);
      PetscOptionsSetValue(nullptr,"-pc_bjacobi_blocks", "1");
    }
    
    if (gpu) {
        VecSetType(pred_system_rhs.petsc_vector(), VECMPICUDA);
        VecSetType(predictor_solution.petsc_vector(), VECMPICUDA);
    }
    
    int ierr;
    pre_precond.clear();
    pre_precond.initialize(predictor_system_matrix);

    PetscOptionsSetValue(nullptr,"-pc_bjacobi_blocks", "1");

    predictor_system_matrix.compress(VectorOperation::add); //SL

    SolverControl solver_control_pre(1000, 1e-6*pred_system_rhs.l2_norm());
    PETScWrappers::SolverCG solver_pre(solver_control_pre);
    predictor_solution=0.0;

    solver_pre.solve(predictor_system_matrix,predictor_solution,pred_system_rhs,pre_precond);
    
     if (gpu){
      Mat cpu_matrix;
      MatConvert(predictor_system_matrix.petsc_matrix(), MATAIJ, MAT_INITIAL_MATRIX, &cpu_matrix);

      dealii::PETScWrappers::MPI::SparseMatrix new_matrix(cpu_matrix);
      predictor_system_matrix.reinit(new_matrix);

      VecSetType(pred_system_rhs.petsc_vector(), VECMPI);  
      VecSetType(predictor_solution.petsc_vector(), VECMPI);
    }
    

    if(verbose)
    {
      std::string tempPreNAME;
      if (timestep_method=="be")
      {
        tempPreNAME = "FE";
      }
      else if (timestep_method=="bdf2")
      {
        tempPreNAME = "EMR";
      }
      else if (timestep_method =="imr")
      {
        tempPreNAME = "AB3";
      }
      pcout << "     " << tempPreNAME <<" predictor solved in " << solver_control_pre.last_step() << " CG iterations." << endl;
    }
    constraints.distribute(predictor_solution);
  }

